    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        if (tbfilhos.getSelectedRow() != -1) {

            Filho u = new Filho();
            FilhoDao dao = new FilhoDao();

            u.setNomeFilho(txtNomeFilho.getText());
            u.setSexoFilho(rbMasculino.getText());
            u.setSexoFilho(rbFeminino.getText());
            dao.update(u);

            txtNomeFilho.setText("");
            txtDataDeNascimentoFilho.setText("");
            rbMasculino.setText("");
            rbFeminino.setText("");

            readJTable();

        }

    }//GEN-LAST:event_btAlterarActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        if (tbfilhos.getSelectedRow() != -1) {

            Filho u = new Filho();
            FilhoDao dao = new FilhoDao();

            u.setIdFilho((int) tbfilhos.getValueAt(tbfilhos.getSelectedRow(), 0));

            dao.delete(u);

            txtNomeFilho.setText("");
            txtDataDeNascimentoFilho.setText("");
            rbMasculino.setText("");
            rbFeminino.setText("");

            readJTable();

        } else {
            JOptionPane.showMessageDialog(null, "Selecione um filho para excluir do cadastro.");
        }

    }//GEN-LAST:event_btExcluirActionPerformed

