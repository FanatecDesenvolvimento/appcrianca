    public void readJTable() {

        DefaultTableModel modelo = (DefaultTableModel) tbfilhos.getModel();
        modelo.setNumRows(0);
        FilhoDao udao = new FilhoDao();

        for (Filho u : udao.read()) {

            modelo.addRow(new Object[]{
                u.getIdFilho(),
                u.getNomeFilho(),
                u.getSexoFilho(),});

        }
    }

    public void readForDesc(String nome) {
        DefaultTableModel modelo = (DefaultTableModel) tbfilhos.getModel();
        modelo.setNumRows(0);
        FilhoDao pdao = new FilhoDao();

        for (Filho u : pdao.readForDesc(nome)) {
            modelo.addRow(new Object[]{
                u.getIdFilho(),
                u.getNomeFilho(),
                u.getSexoFilho(),});
        }
    }

